#include <stdio.h>

//***************************************************
/**
 * 読み込んだ正の整数値を逆順に表示
 */
//***************************************************

int main(void)
{
    int num = 0;

    printf("正の整数を入力して下さい >> ");
    scanf("%d", &num);

    if (num < 0)
    {
        printf("正の整数を入力して下さい >> ");
    }
    else
    {
        while (num > 0)
        {
            // 余りを表示
            // 1桁ずつずらす
            printf("%d", num % 10);
            num /= 10;
        }
    }

    return 0;
}