//***************************************************
/** 文字列の配列 */
//***************************************************

#include <stdio.h>

int main(void)
{
    int i;
    char cs[][6] = {"aiueo\0", "NA", "ABCD"}; //5文字までの文字列で、要素数３つの配列

    for (i = 0; i < 3; i++)
    {
        printf("cs[%d] = %s\n", i, cs[i]);
    }

    return 0;
}