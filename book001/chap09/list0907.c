//***************************************************
/** 文字列の配列を読み込んで表示 */
//***************************************************

#include <stdio.h>

int main(void)
{
    int i;
    char str[3][128]; //全体要素数3、文字列数128までの配列（要素型char[128]）

    for (i = 0; i < 3; i++)
    {
        printf("str[%d] >> ", i);
        scanf("%s", str[i]);
    }

    printf("\n");

    for (i = 0; i < 3; i++)
    {
        printf("str[%d] = %s\n", i, str[i]);
    }

    return 0;
}