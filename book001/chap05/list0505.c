#include <stdio.h>

//***************************************************
/**
 * 配列の各要素を先頭から順に 1,2,3,4,5 で初期化して表示
 */
//***************************************************

int main(void)
{
    int array[5] = {1, 2, 3, 4, 5};

    for (int i = 0; i < 5; i++)
    {
        printf("array[%d] = %d\n", i, array[i]);
    }

    return 0;
}