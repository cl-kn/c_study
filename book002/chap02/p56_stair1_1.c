//***************************************************
/** 入力値分の段を表示する */
//***************************************************

#include <stdio.h>

int main(void)
{
    int i, j, in;

    printf("表示する段数を入力して下さい\n>>");
    scanf("%d", &in);

    for (i = 1; i <= in; i++)
    {
        for (j = 1; j < i; j++)
        {
            putchar(' ');
        }
        printf("%d\n", i);
    }

    return 0;
}