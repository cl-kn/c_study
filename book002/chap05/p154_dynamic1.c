#include <stdio.h>
#include <stdlib.h>

int main(void)
{
    int *x;
    x = calloc(1, sizeof(int)); // 領域確保

    if (x == NULL)
    {
        puts("領域確保に失敗しました");
    }
    else
    {
        // *x = 101;
        printf("*x = %d", *x);
    }

    return 0;
}

/*
calloc()では、確保した領域の全ビットを0で埋めるため、初期値は０と表示される
*/