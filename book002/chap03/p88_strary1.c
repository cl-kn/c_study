//***************************************************
/** 文字列の配列：2次元配列 */
//***************************************************

#include <stdio.h>

int main(void)
{
    char str1[][6] = {
        "Super",
        "X",
        "TRY"};

    for (int i = 0; i < 3; i++)
    {
        printf("%s\n", str1[i]);
    }

    return 0;
}

// 2次元配列
//文字列の長さにかかわらず、配列の大きさを確保するので、無駄ができる。