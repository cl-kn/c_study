#include <stdio.h>
#define NUM 5

int main(void)
{
    float arr[NUM][2] = {{90, 0}, {100, 1}, {55, 2}, {89, 3}, {74, 4}};

    int i, j;

    for (i = 0; i < NUM; i++)
    {
        for (j = 0; j < 2; j++)
        {
            printf("[%.0f]", arr[i][j]);
        }
        puts("\n");
    }

    return 0;
}